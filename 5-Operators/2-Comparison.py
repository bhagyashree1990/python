#If the values of the two operands are equal, then the condition becomes true
print(4==4)
#If values of the two operands are not equal, then the condition becomes true
print(2!=4)
#If the value of the left operand is less than the value of the right operand, then the condition becomes false
print(2>4)
#If the value of the left operand is less than the value of the right operand, then the condition becomes true
print(2<4)
#If the value of the left operand is less than or equal to the value of the right operand, then the condition becomes false
print(2>=4)
#If the value of the left operand is less than or equal to the value of the right operand, then the condition becomes true
print(2<=4)